func mergeWithTwoPointsFromHead(nums1 []int, nums2 []int) (sorted []int) {
	m, n := len(nums1), len(nums2)
	sorted = make([]int, 0, m+n)
	p1, p2 := 0, 0

	for {
		// åˆ°å°½å¤´äº†
		if p1 == m {
			sorted = append(sorted, nums2[p2:]...)
			break
		}

		if p2 == n {
			sorted = append(sorted, nums1[p1:]...)
			break
		}

		if nums1[p1] < nums2[p2] {
			sorted = append(sorted, nums1[p1])
			p1++
		} else {
			sorted = append(sorted, nums2[p2])
			p2++
		}
	}

	return
}

